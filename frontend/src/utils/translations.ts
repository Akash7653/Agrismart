export const useTranslation = (language: string) => {
  const normalize = (lang: string) => {
    if (!lang) return 'en';
    const l = lang.toLowerCase();
    if (l === 'en' || l.startsWith('en') || l.includes('english')) return 'en';
    if (l === 'hi' || l.startsWith('hi') || l.includes('hindi')) return 'hi';
    if (l === 'te' || l.startsWith('te') || l.includes('telugu')) return 'te';
    if (l === 'ta' || l.startsWith('ta') || l.includes('tamil')) return 'ta';
    if (l === 'kn' || l.startsWith('kn') || l.includes('kannada')) return 'kn';
    if (l === 'mr' || l.startsWith('mr') || l.includes('marathi')) return 'mr';
    if (l === 'gu' || l.startsWith('gu') || l.includes('gujarati')) return 'gu';
    if (l === 'bn' || l.startsWith('bn') || l.includes('bengali')) return 'bn';
    if (l === 'pa' || l.startsWith('pa') || l.includes('punjabi')) return 'pa';
    // Malayalam currently reuses Hindi copy
    if (l === 'ml' || l.startsWith('ml') || l.includes('malayalam')) return 'hi';

    // default fallback
    return 'en';
  };

  const t = (key: string): string => {
    const langCode = normalize(language) as keyof typeof translations;
    const parts = key.split('.');
    const get = (obj: unknown, pathParts: string[]) => {
      let cur: unknown = obj;
      for (const p of pathParts) {
        if (cur == null || typeof cur !== 'object') return undefined;
        cur = (cur as Record<string, unknown>)[p];
      }
      return cur as unknown;
    };

    const translation = get(translations[langCode], parts);
    if (typeof translation === 'string') return translation;

    // Fallback to English if translation not found or not a string
    const fallback = get(translations.en, parts);
    if (typeof fallback === 'string') return fallback;

    return key;
  };

  return { t };
};

